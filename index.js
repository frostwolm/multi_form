        const maxDate = new Date;
        maxDate.setDate(nowDate.getDate() + 15);
        const maxDateStr = `${maxDate.getFullYear()}-${maxDate.getMonth() + 1}-${maxDate.getDate()}`;
        const dateElement = document.getElementById('date');
